function chatBox(){
    vBtnID = document.getElementById("chatButton");
    vBoxID = document.getElementById("chatBox");
    vBtnID.style.display = "none";
    vBoxID.style.display = "block";
}
function chatButton(){
    vBtnID = document.getElementById("chatButton");
    vBoxID = document.getElementById("chatBox");
    vBtnID.style.display = "block";
    vBoxID.style.display = "none";
}